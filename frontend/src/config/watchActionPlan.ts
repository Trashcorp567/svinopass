export type WatchActionStep = {
  text: string;
  link?: { href: string; label: string };
};

export function getWatchActionSteps(breachCount: number): WatchActionStep[] {
  if (breachCount > 0) {
    return [
      {
        text: "Смените пароль на каждом сервисе из списка утечек. Если пароль был одинаковым — смените его везде, где использовали.",
      },
      {
        text: "Включите двухфакторную аутентификацию (2FA) на почте, банках и важных аккаунтах.",
      },
      {
        text: "Проверьте, не светился ли ваш пароль в других утечках.",
        link: { href: "/check", label: "Проверка пароля" },
      },
      {
        text: "Создайте уникальные пароли для каждого сервиса — не используйте один пароль дважды.",
        link: { href: "/#pricing", label: "Заказать пароль" },
      },
      {
        text: "Следите за новыми утечками — сторож пришлёт письмо, если email снова попадёт в базы.",
        link: { href: "/watch", label: "Подключить сторожа" },
      },
    ];
  }

  return [
    {
      text: "Пока в публичных базах утечек ваш email не встречался — но новые утечки появляются регулярно.",
    },
    {
      text: "Не используйте один пароль на разных сайтах.",
      link: { href: "/check", label: "Проверить пароль" },
    },
    {
      text: "Подключите мониторинг — раз в неделю проверим базы и сообщим о новых утечках.",
      link: { href: "/watch", label: "Свиной сторож" },
    },
  ];
}

export function getWatchActionDisclaimer(): string {
  return "Удалить данные из уже утёкших баз невозможно — копии разошлись по сети. Важно сменить пароли и не дать злоумышленникам ими воспользоваться.";
}
